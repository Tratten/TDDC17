package tddc17;

import java.util.ArrayList;

public class Node {
//	public int x;
//	public int y;
//	public ArrayList<Node> path;
	public Node parent;
	public boolean explored;
	public int type;
	public int x;
	public int y;
	
	
	public Node(int type, int x, int y){
		this.type = type;
		this.x = x;
		this.y = y;
	}
	
	public String toString(){
		return "X: "+ this.x +" Y: " + this.y + " P: " + this.parent + " D: " + this.explored + " T: " + this.type;
	}
	
}
